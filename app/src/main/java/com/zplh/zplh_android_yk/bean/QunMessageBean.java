package com.zplh.zplh_android_yk.bean;

import java.io.Serializable;

/**
 * Created by zhangshuai on 2017/11/6.
 * Description: 通讯录加好友时，把好友信息传到服务器
 */

public class QunMessageBean implements Serializable {
    private String id; //图片id
    private String group_count;// 群人数
    private String uid;//设备名称

    private  String  number ; //人数

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getGroup_count() {
        return group_count;
    }

    public void setGroup_count(String group_count) {
        this.group_count = group_count;
    }
    public QunMessageBean( String group_count,String id , String uid, String number){
        this.id = id;
        this.group_count = group_count;
        this.uid = uid;
        this.number = number ;
    }
}
